CREATE TABLE PAY_TRADE_ORDER
(
    TRADE_NO VARCHAR(32) PRIMARY KEY NOT NULL,
    OUT_TRADE_NO VARCHAR(64),
    CREATED_TIME INT,
    EXP_DATE INT,
    DEAL_TIME INT,
    PAY_AMOUNT INT(15),
    REFUND_AMOUNT INT(15),
    TRADE_STATE INT(1),
    REFUND_STATE INT(1),
    PAY_IP VARCHAR(256),
    REMARK VARCHAR(256),
    PAY_USER_ID VARCHAR(64),
    CALL_BACK_URL VARCHAR(128),
    NOTIFY_URL VARCHAR(128)
);
CREATE INDEX IDX_KPTN_OUT_NO ON PAY_TRADE_ORDER (OUT_TRADE_NO);
CREATE INDEX IDX_KPTN_PAY_USER ON PAY_TRADE_ORDER (PAY_USER_ID, CREATED_TIME);

CREATE TABLE PAY_CHARGE_ORDER
(
    CHARGE_NO VARCHAR(32) PRIMARY KEY NOT NULL,
    CHARGE_AMOUNT INT(15),
    CHARGE_STATE INT(1),
    CHARGE_TIME INT,
    DEAL_TIME INT,
    USER_ID VARCHAR(64),
    OUT_CHARGE_NO VARCHAR(32),
    PAY_FLAG INT(1),
    REMARK VARCHAR(256)
);

CREATE INDEX IDX_KCHARGE_TIME ON PAY_CHARGE_ORDER (CHARGE_TIME);
CREATE INDEX IDX_KCHARGE_USER_ID ON PAY_CHARGE_ORDER (USER_ID);


CREATE TABLE PAY_MERCHANT_ORDER
(
    PAY_NO VARCHAR(32) PRIMARY KEY NOT NULL,
    OUT_TRADE_NO VARCHAR(64),
    GATE_ORDER_NO VARCHAR(128),
    BANK_ORDER_NO VARCHAR(128),
    CREATED_TIME INT,
    EXP_DATE INT,
    DEAL_TIME INT,
    CALLBACK_TIME INT,
    PAY_AMOUNT INT(15),
    COMMISSION INT(15),
    REAL_AMOUNT INT(15),
    TRADE_STATE INT(1),
    CALLBACK_STATE INT(1),
    REFUND_STATE INT(1),
    REFUND_AMOUNT INT(15),
    BANK_CODE VARCHAR(12),
    BANK_NAME VARCHAR(64),
    PAY_IP VARCHAR(256),
    CALLBACK_URL VARCHAR(256),
    NONTIFY_URL VARCHAR(256),
    CHECK_NO VARCHAR(64),
    TRADE_NO VARCHAR(32),
    REFUND_TIME INT,
    PAY_USER_ID VARCHAR(32),
    REMARK VARCHAR(256)
);

CREATE INDEX IDX_KPMO_OUT_NO ON PAY_MERCHANT_ORDER (OUT_TRADE_NO);
CREATE INDEX IDX_KPMO_USER_AND_TIME ON PAY_MERCHANT_ORDER (PAY_USER_ID, CREATED_TIME);
CREATE INDEX IDX_KPMO_TRADE_NO ON PAY_MERCHANT_ORDER (TRADE_NO);

